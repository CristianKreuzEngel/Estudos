CREATE TABLE EMPRESA (
    ID_EMPRESA NUMERIC(18) PRIMARY KEY NOT NULL,
    CODIGO NUMERIC(5),
    NOME VARCHAR(50)
)

CREATE TABLE SETOR (
    ID_SETOR NUMERIC(18) PRIMARY KEY NOT NULL,
    CODIGO NUMERIC(5),
    NOME VARCHAR(50),
    FK_IDEMPRESA NUMERIC(18) REFERENCES EMPRESA(ID_EMPRESA)
)

CREATE TABLE EMPREGADO (
    ID_EMPREGADO NUMERIC(18) PRIMARY KEY NOT NULL,
    CODIGO NUMERIC(5),
    NOME VARCHAR(50),
    CPF VARCHAR(11),
    DATA_ADIMISSAO TIMESTAMP,
    FK_IDEMPRESA NUMERIC(18) REFERENCES EMPRESA(ID_EMPRESA)
)

CREATE TABLE SALARIO (
    ID_SALARIO NUMERIC(18) PRIMARY KEY NOT NULL,
    CODIGO NUMERIC(5),
    VALOR NUMERIC(10,2)
)

CREATE TABLE HISTORICO_SALARIO (
    ID_HISTORICO NUMERIC(18) PRIMARY KEY NOT NULL,
    FK_IDSETOR NUMERIC(18) REFERENCES SETOR(ID_SETOR),
    FK_IDSALARIO NUMERIC(18) REFERENCES SALARIO(ID_SALARIO),
    DATA_INICIO TIMESTAMP,
    DATA_FIM TIMESTAMP,
    FK_IDEMPREGADO NUMERIC(18) REFERENCES EMPREGADO(ID_EMPREGADO)
)

